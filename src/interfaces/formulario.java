    private void BenviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BenviarActionPerformed
      Connection conn = null;
      
        try {
            conn = getConnection();
            ps = conn.prepareStatement("INSERT INTO registro (fechareg, distrito, instancia,especialidad,subespecialidad,baselegal)VALUES (?,?,?,?,?,?)");
            ps.setDate(1, Date.valueOf(LocalDate.now()));
            ps.setString(2, CajaDistrito.getText());
            ps.setString(3, CajaInstancia.getText());
            ps.setString(4, CajaEspecialidad.getText());
            ps.setString(5, CajaSubespecialidad.getText());
            ps.setString(6, CajaBaselegal.getText());
            
            
            int result = ps.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(null, "Los datos fueron enviados correctamente");
                borrarCajas ();
                
            }else{
                JOptionPane.showMessageDialog(null, "Los datos no fueron enviados");
              }
            conn.close ();
            
        } catch (Exception e) {
            System.out.println (e);
        }
    }//GEN-LAST:event_BenviarActionPerformed

